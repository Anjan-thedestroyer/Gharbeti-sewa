export function searchPlaces(searchTerm = '', limit = 10) {
  const term = (searchTerm || '').toString().toLowerCase().trim();

  // Filter with proper null checks
  const results = allPlaces.filter(place => {
    try {
      return place?.properties?.name?.toLowerCase().includes(term);
    } catch (e) {
      console.warn("Invalid place data:", place);
      return false;
    }
  });

  return results.slice(0, limit);
}

